export function extractCustomerName(message: string): string {
  // First, try to extract name from specific patterns like "a nombre de", "reservado por", etc.
  const nameIndicatorPatterns = [
    // "a nombre de X" or "al nombre de X"
    /a\s+(?:l\s+)?nombre\s+de\s+([A-ZÁÉÍÓÚÑÜ][a-záéíóúñü]+(?:\s+[A-ZÁÉÍÓÚÑÜ][a-záéíóúñü]+)*)/i,
    // "reservado por X" or "reserva por X"
    /reservad[ao]?\s+por\s+([A-ZÁÉÍÓÚÑÜ][a-záéíóúñü]+(?:\s+[A-ZÁÉÍÓÚÑÜ][a-záéíóúñü]+)*)/i,
    // "reserva de X" or "reserva para X"
    /reserva\s+(?:de|para)\s+([A-ZÁÉÍÓÚÑÜ][a-záéíóúñü]+(?:\s+[A-ZÁÉÍÓÚÑÜ][a-záéíóúñü]+)*)/i,
    // "cita para X" or "cita de X"
    /cita\s+(?:para|de)\s+([A-ZÁÉÍÓÚÑÜ][a-záéíóúñü]+(?:\s+[A-ZÁÉÍÓÚÑÜ][a-záéíóúñü]+)*)/i,
    // "mesa para X" or "mesa de X"
    /mesa\s+(?:para|de)\s+([A-ZÁÉÍÓÚÑÜ][a-záéíóúñü]+(?:\s+[A-ZÁÉÍÓÚÑÜ][a-záéíóúñü]+)*)/i,
    // "evento para X" or "evento de X"
    /evento\s+(?:para|de)\s+([A-ZÁÉÍÓÚÑÜ][a-záéíóúñü]+(?:\s+[A-ZÁÉÍÓÚÑÜ][a-záéíóúñü]+)*)/i,
    // "de parte de X"
    /de\s+parte\s+de\s+([A-ZÁÉÍÓÚÑÜ][a-záéíóúñü]+(?:\s+[A-ZÁÉÍÓÚÑÜ][a-záéíóúñü]+)*)/i,
    // "bajo el nombre de X"
    /bajo\s+(?:el\s+)?nombre\s+de\s+([A-ZÁÉÍÓÚÑÜ][a-záéíóúñü]+(?:\s+[A-ZÁÉÍÓÚÑÜ][a-záéíóúñü]+)*)/i,
    // "booking for X" or "reservation for X" (English)
    /(?:booking|reservation)\s+for\s+([A-ZÁÉÍÓÚÑÜ][a-záéíóúñü]+(?:\s+[A-ZÁÉÍÓÚÑÜ][a-záéíóúñü]+)*)/i,
    // "under the name of X" or "under name X" (English)
    /under\s+(?:the\s+)?name\s+(?:of\s+)?([A-ZÁÉÍÓÚÑÜ][a-záéíóúñü]+(?:\s+[A-ZÁÉÍÓÚÑÜ][a-záéíóúñü]+)*)/i,
  ];

  for (const pattern of nameIndicatorPatterns) {
    const match = message.match(pattern);
    if (match && match[1]) {
      const extractedName = match[1].trim();
      if (!isCommonWord(extractedName)) {
        return extractedName;
      }
    }
  }

  // Fallback: extract capitalized words that look like names
  const namePattern =
    /[A-ZÁÉÍÓÚÑÜ][a-záéíóúñü]+(?:\s+[A-ZÁÉÍÓÚÑÜ][a-záéíóúñü]+)*/g;
  const matches = message.match(namePattern) || [];

  const names = matches.filter((n) => !isCommonWord(n));
  return names.length > 0 ? names[0] : "";
}

